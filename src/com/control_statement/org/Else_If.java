package com.control_statement.org;

public class Else_If {
	
	public static void main(String[] args) {
		
		int a=10,b=20;
		
		if (a>b) {
			System.out.println("yes");
			
		}
		else if (a==b) {
			System.out.println("no");
			
		}
		else if (a<b) {
			System.out.println("yes");
			
		}
		else if (a>=b) {
			System.out.println("no");
			
		}
		else if (a<=b) {
			
			System.out.println("yes");
			
		}
	}

}
